package lect06;

public class Student {
	
	private long id;
	private String name;
	private String surname;
	
	// setter id
	public void setId(long id) {
		this.id = id;
	}
	// getter id
	public long getId() {
		return this.id;
	}
	// setter name
	public void setName(String name) {
		this.name = name;
	}
	//getter name
	public String getName() {
		return this.name;
	}
	//setter surname
	public void setSurname(String surname) {
		this.surname = surname;
	}
	//getter surname
	public String getSurname() {
		return this.surname;
	}

}
